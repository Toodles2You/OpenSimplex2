#include <stdio.h>

static void write_gradients(const char *filename, float *gradients, int length) {

    FILE *f = fopen(filename, "w");
    if (f != NULL)
    {
        printf("Writing \"%s\"\n", filename);
        for (int i = 0; i < length; i++)
        {
            fprintf(f, "%ff,\n", gradients[i]);
        }
        fclose(f);
    }
}

static void write_lookup(const char *filename, int *lookup, int length) {

    FILE *f = fopen(filename, "w");
    if (f != NULL)
    {
        printf("Writing \"%s\"\n", filename);
        for (int i = 0; i < length; i++)
        {
            fprintf(f, "%i,\n", lookup[i]);
        }
        fclose(f);
    }
}

static void write_lattice(const char *filename, LatticeVertex4D *lattice, int length) {

    FILE *f = fopen(filename, "w");
    if (f != NULL)
    {
        printf("Writing \"%s\"\n", filename);
        for (int i = 0; i < length; i++)
        {
            fprintf(f, "{%ff, %ff, %ff, %ff, %liL, %liL, %liL, %liL},\n",
                lattice[i].dx, lattice[i].dy,
                lattice[i].dz, lattice[i].dw,
                lattice[i].xsvp, lattice[i].ysvp,
                lattice[i].zsvp, lattice[i].wsvp);
        }
        fclose(f);
    }
}

