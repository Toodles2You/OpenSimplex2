/*
 * Utility
 */

static void LatticeVertex4D_initialize(LatticeVertex4D *this, int xsv, int ysv, int zsv, int wsv) {
    *(long *)&this->xsvp = xsv * S_PRIME_X; *(long *)&this->ysvp = ysv * S_PRIME_Y;
    *(long *)&this->zsvp = zsv * S_PRIME_Z; *(long *)&this->wsvp = wsv * S_PRIME_W;
    float ssv = (xsv + ysv + zsv + wsv) * S_UNSKEW_4D;
    *(float *)&this->dx = -xsv - ssv;
    *(float *)&this->dy = -ysv - ssv;
    *(float *)&this->dz = -zsv - ssv;
    *(float *)&this->dw = -wsv - ssv;
}

static void LatticeVertex4D_copy(LatticeVertex4D *this, LatticeVertex4D *other) {
    *(long *)&this->xsvp = other->xsvp; *(long *)&this->ysvp = other->ysvp;
    *(long *)&this->zsvp = other->zsvp; *(long *)&this->wsvp = other->wsvp;
    *(float *)&this->dx = other->dx;
    *(float *)&this->dy = other->dy;
    *(float *)&this->dz = other->dz;
    *(float *)&this->dw = other->dw;
}

