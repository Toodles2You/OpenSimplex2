    write_gradients(GRADIENTS_3D_SRC, GRADIENTS_3D, lengthof(GRADIENTS_3D));
    write_gradients(GRADIENTS_4D_SRC, GRADIENTS_4D, lengthof(GRADIENTS_4D));

    OpenSimplex2S_initialize();

    write_gradients(S_GRADIENTS_2D_SRC, S_GRADIENTS_2D, lengthof(S_GRADIENTS_2D));
    write_gradients(S_GRADIENTS_3D_SRC, S_GRADIENTS_3D, lengthof(S_GRADIENTS_3D));
    write_gradients(S_GRADIENTS_4D_SRC, S_GRADIENTS_4D, lengthof(S_GRADIENTS_4D));

    write_lookup(S_LOOKUP_4D_A_SRC, LOOKUP_4D_A, lengthof(LOOKUP_4D_A));
    write_lattice(S_LOOKUP_4D_B_SRC, LOOKUP_4D_B, lengthof(LOOKUP_4D_B));

    puts("Done!");

